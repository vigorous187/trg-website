#!/usr/bin/env node
/**
 * Sync Mystic SEO rankings into TRG site data for the public results dashboard.
 *
 *   node scripts/sync-mystic-seo-report.mjs
 *   MYSTIC_SEO_DIR=/path/to/seo-rankings node scripts/sync-mystic-seo-report.mjs
 */
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const DEFAULT_MYSTIC_DIR = path.resolve(
  ROOT,
  "../../clients/mystic-caribbean-react/scripts/seo-rankings",
);
const MYSTIC_DIR = process.env.MYSTIC_SEO_DIR || DEFAULT_MYSTIC_DIR;
const OUT = path.join(ROOT, "src", "data", "mystic-seo-report.ts");

function rankDisplay(rank) {
  if (rank == null || rank >= 101) return null;
  return rank;
}

function formatChange(current, previous) {
  if (current == null || previous == null) return null;
  if (current >= 101 || previous >= 101) return null;
  return previous - current;
}

async function main() {
  const history = JSON.parse(
    await readFile(path.join(MYSTIC_DIR, "history.json"), "utf8"),
  );
  const config = JSON.parse(
    await readFile(path.join(MYSTIC_DIR, "mystic-keywords.json"), "utf8"),
  );

  const snapshots = history.snapshots || [];
  const latest = snapshots.at(-1);
  const previous = snapshots.at(-2);
  if (!latest?.ranks) {
    throw new Error("No rankings snapshot found in Mystic history.json");
  }

  const tracked = (config.keywords || []).filter((k) => k.track !== false);
  const keywords = tracked
    .map((k) => {
      const rank = rankDisplay(latest.ranks[k.keyword]);
      const prevRank = rankDisplay(previous?.ranks?.[k.keyword]);
      return {
        keyword: k.keyword,
        category: k.category || "core",
        rank,
        previousRank: prevRank,
        change: formatChange(
          latest.ranks[k.keyword],
          previous?.ranks?.[k.keyword],
        ),
      };
    })
    .filter((row) => row.rank != null)
    .sort((a, b) => a.rank - b.rank);

  const report = {
    clientName: config.site_name || "Mystic Caribbean Resto & Bar",
    domain: config.domain || history.domain || "mysticrestobar.com",
    location: "Brampton, ON",
    reportDate: latest.date,
    baseline: {
      total: config.seo_scores?.before?.total ?? 32,
      label: config.seo_scores?.before?.label ?? "Before Forge rebuild",
      date: config.seo_scores?.before?.date ?? "2026-04-08",
      issues: config.seo_scores?.before?.issues ?? [],
    },
    current: {
      total: latest.seo_score?.total ?? null,
      technical: latest.seo_score?.technical ?? null,
      visibility: latest.seo_score?.visibility ?? null,
      inTop10:
        latest.seo_score?.inTop10 ??
        keywords.filter((k) => k.rank <= 10).length,
      keywordCount: latest.seo_score?.keywordCount ?? tracked.length,
      technicalChecks: latest.seo_score?.technicalChecks ?? [],
    },
    dashboardUrl: "https://dashboard.forge-co.ca/client/mystic-caribbean",
    keywords,
  };

  const body = `/** Auto-generated by scripts/sync-mystic-seo-report.mjs — do not edit by hand. */

export const mysticSeoReport = ${JSON.stringify(report, null, 2)} as const;

export type MysticSeoReport = typeof mysticSeoReport;
`;

  await writeFile(OUT, body, "utf8");
  console.log(
    `Wrote ${OUT} (${keywords.length} ranked keywords, score ${report.current.total}/100, ${report.current.inTop10} in top 10)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
